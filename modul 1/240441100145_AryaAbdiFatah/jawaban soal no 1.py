   #disini saya memasukan variabel yang di ketahui
diameter = 14
r =diameter/2
print("jari jari tabung adalah=", r, "cm")
l = 440 #luas keliling tabung

phi = 22/7
t = l/2*phi*r
# disini saya ingin memunculkan nilai tinggi dari sebuah rumus diatas
print("tinggi tabung adalah=", t, "cm")
volume = phi*r*r*t
# disini saya ingin memunculkan nilai volume tabung
print("volume tabung adalah =", volume, "cm3") 


