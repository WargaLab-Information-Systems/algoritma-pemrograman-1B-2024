# Volume Balok
panjang = 20
lebar = 13
tinggi_balok = 7
volume_balok = panjang * lebar * tinggi_balok
print(f"Volume Celengan Berbentuk Balok: {volume_balok} cm³")

# Volume Tabung
diameter = 14
jari_jari = diameter / 2
luas_selimut = 440
pi = 3.14
tinggi_tabung = luas_selimut / (2 * pi * jari_jari)
volume_tabung = pi * jari_jari**2 * tinggi_tabung
print(f"Volume Celengan Berbentuk Tabung: {volume_tabung} cm³")